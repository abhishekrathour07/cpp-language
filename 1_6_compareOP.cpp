#include <iostream>
using namespace std;

int main()
{

    int a = 8, b = 6;
    cout << "These are comparison operator " << endl;
    cout << "The value of (a==b) is " << (a == b) << endl;
    cout << "The value of (a!=b) is " << (a != b) << endl;
    cout << "The value of (a>=b) is " << (a >= b) << endl;
    cout << "The value of (a<=b) is " << (a <= b) << endl;
    return 0;
}