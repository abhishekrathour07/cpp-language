#include <iostream>
using namespace std;

int main()
{
    int a = 6, b = 9;
    cout << "These are arthematic operators " << endl;
    cout << "The value of (a+b) is " << (a + b) << endl;
    cout << "The value of (a-b) is " << (a - b) << endl;
    cout << "The valuee of (a*b) is " << (a * b) << endl;
    cout << "the value of (a/b) is " << (a / b) << endl;
    cout << "the value of a++ is " << a++ << endl;
    cout << "The value  of a-- is " << a-- << endl;
    cout << "The value of ++a is " << ++a << endl;
    cout << "The value of --a is " << --a << endl;
    return 0;
}